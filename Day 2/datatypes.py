# Subscripting
print("Hello"[0])
print("Hello"[-1])
print("123" + "456")
print(type("Hello"))

# Integer
print(1)
print(1 + 2)
print(type(1))

# Large Integers
print(123_456_789) # It`s just like 123456789
print(type(123_456_789))

# Float
print(3.1415)
print(type(3.1415))

# Boolean
print(True)
print(False)
print(type(True))